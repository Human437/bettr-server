CREATE TABLE workouts (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  phase TEXT NOT NULL,
  week INTEGER NOT NULL,
  day INTEGER NOT NULL,
  type TEXT NOT NULL,
  sub_type TEXT,
  main_movements TEXT [],
  acc_movements TEXT [],
  main_rep_scheme TEXT,
  main_reps TEXT,
  main_sets TEXT,
  main_rest TEXT,
  acc_reps TEXT,
  acc_sets TEXT,
  acc_rest TEXT,
  acc_distance TEXT,
  acc_time TEXT,
  main TEXT [],
  acc TEXT [],
  warm_up TEXT,
  focus TEXT,
  completed BOOLEAN NOT NULL,
  cycle_id INTEGER REFERENCES cycles(id) ON DELETE CASCADE NOT NULL,
  created_at TIMESTAMP NOT NULL DEFAULT now(),
  updated_at TIMESTAMP NOT NULL DEFAULT now()
);